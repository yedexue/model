package sqldemo.model;
public class MapElements {
	int id;
	String sourcemodelType;
	String sourcemodelname;
	String modeltype_corr;
	String modelname_corr;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getSourcemodelType() {
		return sourcemodelType;
	}

	public void setSourcemodelType(String sourcemodelType) {
		this.sourcemodelType = sourcemodelType;
	}

	public String getSourcemodelname() {
		return sourcemodelname;
	}

	public void setSourcemodelname(String sourcemodelname) {
		this.sourcemodelname = sourcemodelname;
	}

	public String getModeltype_corr() {
		return modeltype_corr;
	}

	public void setModeltype_corr(String modeltype_corr) {
		this.modeltype_corr = modeltype_corr;
	}

	public String getModelname_corr() {
		return modelname_corr;
	}

	public void setModelname_corr(String modelname_corr) {
		this.modelname_corr = modelname_corr;
	}

}
